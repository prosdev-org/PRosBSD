typedef struct {
    uint8_t code;
    uint16_t w, h;
    uint8_t bpp;
    uint8_t hidden_dac;
    const uint16_t *seq;
    const uint16_t *graph;
    const uint16_t *crtc;
} cirrus_mode_t;

